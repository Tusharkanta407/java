public class complexPattern {
    public static void main(String[] args) {
        
        for(int i=5;i>0;i-- ){
            for(int j=i;j>0;j--){
                System.out.print(" ");
                
                }
                for(int k=0;k<5;k++){
                    System.out.print("*");
                }
            }
            System.out.println();
        }
    }

    

