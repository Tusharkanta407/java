    public static void main(String[] args) {
        ///for loop print even odd numbers from 1 to 1000
        for (int i = 1; i <= 1000; i++) {
            if(i%2==0){
                System.out.println("Even number is: "+i);
            }else{
                System.out.println("Odd number is: "+i);
            }
        }
        
    }
    
